export default function Sidebar({ isOpen, onClose }){
  const location = useLocation();
  const { user } = useContext(AuthContext);
  const menuGroups = user ? ROLE_MENU[user.role] || [] : [];

  if (!user) return null;

  return (
    <>
      {isOpen && (
        <div 
          onClick={onClose} 
          className="fixed inset-0 z-40 bg-slate-950/40 backdrop-blur-sm lg:hidden transition-opacity duration-300"
        />
      )}

      <aside 
        className={`fixed inset-y-0 left-0 z-40 w-64 bg-slate-900 text-slate-400 border-r border-slate-800 p-4 flex flex-col transition-transform duration-300 ease-in-out transform 
        ${isOpen ? 'translate-x-0' : '-translate-x-full'} 
        lg:relative lg:translate-x-0 lg:z-0 h-[calc(100vh-4.5rem)]`}
      >
        <div className="flex-1 overflow-y-auto pr-1 py-2">
          <nav className="space-y-5">
            {menuGroups.map((group) => (
              <div key={group.title} className="space-y-1.5">
                <h3 className="px-3 text-[10px] font-bold text-slate-500 uppercase tracking-widest select-none">
                  {group.title}
                </h3>
                <ul className="space-y-0.5">
                  {group.items.map((item) => {
                    const isActive = location.pathname === item.path;
                    return (
                      <li key={item.path}>
                        <Link 
                          to={item.path}
                          onClick={onClose}
                          className={`flex items-center gap-3 px-3 py-2 rounded-xl text-xs font-medium transition-all duration-200 group
                          ${isActive 
                            ? 'bg-indigo-600 text-white shadow-md shadow-indigo-600/10' 
                            : 'hover:bg-slate-800/40 hover:text-slate-200'}`}
                        >
                          <svg 
                            className={`w-4 h-4 transition-colors duration-200 
                            ${isActive ? 'text-white' : 'text-slate-500 group-hover:text-slate-300'}`} 
                            fill="none" 
                            viewBox="0 0 24 24" 
                            stroke="currentColor"
                          >
                            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d={item.icon} />
                          </svg>
                          <span>{item.label}</span>
                        </Link>
                      </li>
                    );
                  })}
                </ul>
              </div>
            ))}
          </nav>
        </div>
      </aside>
    </>
  );
}
